			Prepare white sauce:

Melt butter in medium-size saucepan over medium-low heat. Whisk in the flour until well blended. Cook, stirring, for 3 minutes. Gradually whisk in the milk. Whisk in the salt. Heat to boiling over medium heat. Reduce the heat to low; simmer, whisking often, for about 30 minutes or until the sauce is thick. Stir in the nutmeg; cover the surface directly with plastic wrap and set aside.<br/>
Meanwhile, prepare the lasagna:<br/>

Heat 2 teaspoons of the oil in a large skillet over medium heat. Add the onion, garlic and carrot; saute for 3 minutes. Remove the vegetables to a medium-size bowl. Carefully wipe out the skillet.<br/>
Heat 2 teaspoons of the oil in the same skillet over medium heat. Add the sweet red pepper; saute for 3 minutes. Remove the red pepper to the bowl with the onion mixture. Wipe out the skillet again.<br/>
Heat the remaining 2 teaspoons of oil in the skillet. Add the zucchini; saute for 5 minutes. Add the mushrooms; saute for 3 minutes. Add all the vegetables in the bowl back into the skillet. Add the tomatoes, olives, salt and pepper. Bring to boiling. Cover the skillet; lower the heat and simmer the mixture for 15 minutes to blend the flavors. Stir in the basil.<br/>
Heat the oven to 375 degrees F. Lightly grease a 13 x 9 x 2-inch baking dish.<br/>
Cook the noodles in a pot of boiling water following the package directions. Drain the noodles. Combine the mozzarella, provolone and 1/4 cup of the Parmesan cheese in a medium-size bowl.<br/>
To assemble:<br/>

Spread 1/2 cup of the vegetables over bottom of the prepared baking dish. Lay 3 noodles lengthwise over the vegetables in the dish. Spread half of the remaining vegetables over the noodles. Spread one-third of white sauce over the vegetables. Sprinkle with half of the mozzarella cheese mixture. Top with 3 more noodles. Spread remaining vegetables over the top. Spread half of the remaining white sauce over the vegetables.<br/> Sprinkle with the remaining mozzarella cheese mixture. Top with the 3 remaining noodles. Spread the remaining white sauce over the top. Sprinkle with remaining grated Parmesan cheese..
Bake in 375 degree F oven for 30 minutes or until the top is golden brown. Let the lasagna stand for at least 10 minutes before cutting into portions and serving. Makes 9 servings.
